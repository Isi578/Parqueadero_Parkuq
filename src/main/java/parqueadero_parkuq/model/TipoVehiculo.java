package parqueadero_parkuq.model;

/**
 * Enumeración que define los diferentes tipos de vehículos que pueden ingresar al parqueadero.
 */
public enum TipoVehiculo {

    /**
     * Vehículo de tipo carro.
     */
    CARRO,

    /**
     * Vehículo de tipo motocicleta.
     */
    MOTOCILETA,

    /**
     * Vehículo de tipo bicicleta.
     */
    BICICLETA
}