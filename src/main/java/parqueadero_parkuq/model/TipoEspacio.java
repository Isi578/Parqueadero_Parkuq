package parqueadero_parkuq.model;

/**
 * Enumeración que define los diferentes tipos de espacios de estacionamiento disponibles.
 */
public enum TipoEspacio {

    /**
     * Espacio destinado para un carro.
     */
    CARRO,

    /**
     * Espacio destinado para una motocicleta.
     */
    MOTOCICLETA,

    /**
     * Espacio destinado para una bicicleta.
     */
    BICICLETA
}